#include "libft.h"

void	ft_putstr_fd2(char **s, int fd)
{
	int i;

	i = 0;
	while (s[i])
	{
		write(fd, s[i], ft_strlen(s[i]));
		write(fd, "\n", 1);
		i++;
	}
}
